# 372. 循环数 

## Question description


142857是一个六位数，我们发现：
 142857 * 1 = 142857
 142857 * 2 = 285714
 142857 * 3 = 428571
 142857 * 4 = 571428
 142857 * 5 = 714285
 142857 * 6 = 857142
 即用1到6的整数去乘142857，会得到一个将原来的数首尾相接循环移动若干数字再在某处断开而得到的数字。
 也就是说，如果把原来的数字和新的数字都首尾相接，他们得到的环是相同的。只是两个数的起始数字不一定相同。
 请写一个程序，判断给定的数不是循环数。




